// Veckratni izpis besedila "Java je zakon" 
// z uporabo zanke
public class JavaZakon {

	public static void main(String [] args) {
		int i;  // stevec za zanko

		// "sestavni deli" zanke:
		// inicializacija; izstopni pogoj; sprememba stevca
		for(i=0;  i < 10;  i=i+1) {
      System.out.println("Java je zakon!");
    }

    System.out.println(i);
	}
	
}